package com.cricket.containers;

public class ImpactData {
	int inBowlPlayerId;
	int inBatPlayerId;
	int outPlayerId;
	int teamId;
	
	
	public int getInBowlPlayerId() {
		return inBowlPlayerId;
	}
	public void setInBowlPlayerId(int inBowlPlayerId) {
		this.inBowlPlayerId = inBowlPlayerId;
	}
	public int getInBatPlayerId() {
		return inBatPlayerId;
	}
	public void setInBatPlayerId(int inBatPlayerId) {
		this.inBatPlayerId = inBatPlayerId;
	}
	public int getOutPlayerId() {
		return outPlayerId;
	}
	public void setOutPlayerId(int outPlayerId) {
		this.outPlayerId = outPlayerId;
	}
	public int getTeamId() {
		return teamId;
	}
	public void setTeamId(int teamId) {
		this.teamId = teamId;
	}
	
	
}
